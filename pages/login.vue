<template>
  <div class="root">
    <!-- Login Form -->
    <Form v-bind="formEmailPassword" v-if="!options.isLoggedIn" />
    <!-- Show the profile -->
    <div class="form loggedin" v-else="options.profile.username">
      <span>Signed in as</span>
      <img :src="options.profile.avatar_url" />
      <h1>{{ options.profile.username }}</h1>
      <button class="primary-btn btn2" @click="proceed()">
        Proceed to the app
      </button>
      <button class="primary-btn btn2 bottom-element" @click="logout()">
        Sign Out
      </button>
    </div>
    <!-- Disabled -->
    <div class="form loggedin" v-if="false">
      <span>Signed in as</span>
      <img class="disabled" :src="options.profile.avatar_url" />
      <h1>Loading...</h1>
      <button class="primary-btn btn2" disabled>Proceed to the app</button>
      <button class="primary-btn btn2 bottom-element" disabled>Sign Out</button>
    </div>
  </div>
</template>

<script lang="ts" setup>
import { User } from "@firebase/auth";
import { getAuth, onAuthStateChanged } from "@firebase/auth";
import { initializeApp, getApps } from "@firebase/app";

const user = ref<User | null>(null);
const router = useRouter();

interface opt {
  isLoggedIn: boolean;
  profile: any;
}
let options: opt = reactive({
  isLoggedIn: false,
  profile: "",
});

watchEffect(async () => {
  if (user.value) {
    options.isLoggedIn = true;
    options.profile = {
      avatar_url: user.value.photoURL,
      username: user.value.displayName,
    };
  } else {
    options.isLoggedIn = false;
  }
});

onMounted(() => {
  if (getApps().length === 0) {
    const config = useRuntimeConfig();
    // console.log("Firebase app not found, initialising it now");
    initializeApp({
      apiKey: config.APIKEY,
    });
  }

  const auth = getAuth();

  user.value = auth.currentUser;
  onAuthStateChanged(auth, (e) => {
    user.value = e;
    console.log(user.value.photoURL)
  });
});

let inputEmail = ref("");
let inputPassword = ref("");

function proceed() {
  router.push("/chat");
}

async function login(email: string, password: string) {
  try {
    const { user: fbUser } = await signInUser(email, password);
    user.value = fbUser;
  } catch (e) {
    throw e;
  }
}

async function logout() {
  try {
    await signOutUser();
  } catch (e) {
    console.error(e);
  }
}

async function loginHandler(e: SubmitEvent) {
  e.preventDefault();

  let email = inputEmail.value;
  let password = inputPassword.value;

  try {
    await login(email, password);
  } catch (e) {
    console.error(e);

    alert(e.message);
  }
}

onMounted(() => {});

const formEmailPassword = {
  headingNormal: "Sign in to",
  headingHighlight: "start chatting",
  inputs: [
    {
      ref: inputEmail,
      name: "Email",
      type: "email",
    },
    {
      ref: inputPassword,
      name: "Password",
      type: "password",
    },
  ],
  submitText: "Sign In",
  submitEvent: loginHandler,
};
</script>

